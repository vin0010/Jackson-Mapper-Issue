package com.codex.jacksonautowiring;

public class Auto extends Vehicle {
    private String autoType;
    private String autoDriverName;


    public String getAutoType() {
        return autoType;
    }

    public void setAutoType(String autoType) {
        this.autoType = autoType;
    }

    public String getAutoDriverName() {
        return autoDriverName;
    }

    public void setAutoDriverName(String autoDriverName) {
        this.autoDriverName = autoDriverName;
    }

}
